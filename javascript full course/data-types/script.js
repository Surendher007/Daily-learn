/*Data types

// There are three data type

// 1.var
   *var is global scope
   *reclare the value & reassign the value
   *

// 2.let
   *let is a local scope
   *only for reassign the value not for redeclare

// 3.const
   *const is never changed
   *not for reassign and reclare

*/

